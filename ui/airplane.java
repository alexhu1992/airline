package com.airline.ui;

public class airplane {
	
	String id=null;
	String atn=null;
	String ms=null;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAtn() {
		return atn;
	}
	public void setAtn(String atn) {
		this.atn = atn;
	}
	public String getMs() {
		return ms;
	}
	public void setMs(String ms) {
		this.ms = ms;
	}

}
